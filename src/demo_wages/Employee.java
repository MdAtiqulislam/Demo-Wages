    private void setSelectedDataByUser(String cardNumber) {
        
                try {

            String sql = "select * from employee where card_no='" + cardNumber + "'";
            PreparedStatement st = connection.prepareStatement(sql);
            ResultSet rs = st.executeQuery();

            if (rs.next()) {
                token_no_tf.setText(String.valueOf(rs.getInt("card_no")));
                name_tf.setText(rs.getString("name"));
                fathers_name_tf.setText(rs.getString("father_name"));
                address_tf.setText(rs.getString("address"));
                wagesRate=rs.getInt("wages_rate");
                wagesRateTF.setText(String.valueOf(rs.getInt("wages_rate")));
                special_allow_tf.setText(String.valueOf(rs.getInt("special_allowance")));
                int releiverStatus = rs.getInt("reliever_status");
                if (releiverStatus == 1) {
                    reliever_chbox.setSelected(true);
                    reliever_allo_lbl.setEnabled(true);
                    sr_reliever_rb.setEnabled(true);
                    reliever_rb.setEnabled(true);
                    jr_reliever_rb.setEnabled(true);
                    reliever_allo_tf.setEnabled(true);
                    reliever_tk_lbl.setEnabled(true);
                } else {
                    reliever_chbox.setSelected(false);
                    reliever_allo_lbl.setEnabled(false);
                    sr_reliever_rb.setEnabled(false);
                    reliever_rb.setEnabled(false);
                    jr_reliever_rb.setEnabled(false);
                    reliever_allo_tf.setEnabled(false);
                    reliever_tk_lbl.setEnabled(false);
                }

                String nDepartment = rs.getString("department");
                if (!nDepartment.isEmpty()) {
                    department_combo.setSelectedItem(nDepartment);
                } else {
                    department_combo.setSelectedItem("Select One");
                }

                String nDesignation = rs.getString("designation");
                if (!nDesignation.isEmpty()) {
                    designation_combo.setSelectedItem(nDesignation);
                } else {
                    designation_combo.setSelectedItem("Select One");
                }

                String nEmployeeType = rs.getString("employee_type");
                if (nEmployeeType != null) {
                    employee_type_combo.setSelectedItem(nEmployeeType);
                } else {
                    employee_type_combo.setSelectedItem("Select One");
                }

                String nPayableCode = rs.getString("pay_able_code");
                if (nPayableCode != null) {
                    payable_code_combo.setSelectedItem(nPayableCode);
                } else {
                    payable_code_combo.setSelectedItem("Select One");
                }

                String nGender = rs.getString("gender");

                if (nGender.equals("Male") || nGender.equals("male")) {
                    male_radio.setSelected(true);
                } else if (nGender.equals("Female") || nGender.equals("female")) {
                    female_radio.setSelected(true);
                }

                String nRelieverType = rs.getString("reliever_type");
                if (nRelieverType != null && nRelieverType.equals("Sr. Reliever")) {
                    sr_reliever_rb.setSelected(true);
                } else if (nRelieverType != null && nRelieverType.equals("Reliever")) {
                    reliever_rb.setSelected(true);
                } else if (nRelieverType != null && nRelieverType.equals("Jr. Reliever")) {
                    jr_reliever_rb.setSelected(true);
                }
                reliever_allo_tf.setText(String.valueOf(rs.getInt("reliever_allowance")));

                java.sql.Date nJoinningDate = rs.getDate("joining_date");
                if (nJoinningDate != null) {
                    joining_date_dc.setDate(nJoinningDate);
                } else {
                    joining_date_dc.setCalendar(null);
                }
                java.sql.Date desrtionDate = rs.getDate("date_of_desertion");
                if (desrtionDate != null) {
                    desertion_date_dc.setDate(desrtionDate);
                } else {
                    desertion_date_dc.setCalendar(null);
                }
                String active_status = rs.getString("active_status");
                if (active_status != null) {
                    active_status_combo.setSelectedItem(active_status);
                } else {
                    active_status_combo.setSelectedItem("Select One");
                }
                extra_allowance_tf.setText(String.valueOf(rs.getInt("extra_allowance")));
                otRateTF.setText(String.valueOf(rs.getInt("ot_rate")));

                st.close();
                rs.close();
            } else {
                JOptionPane.showMessageDialog(this, "Employee not found.");
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
            }

