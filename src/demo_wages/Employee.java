    private void addEmployee() {
        getData();
        java.sql.Date sqlJoinDate = null, sqlDesertionDate = null;
        if (joinningDate != null) {
            sqlJoinDate = new java.sql.Date(joinningDate.getTime());
        }
        if (desertionDate != null) {
            sqlDesertionDate = new java.sql.Date(desertionDate.getTime());
        }

        if (error_message_lbl.getText().isEmpty()) {

            try {
                String sql = "select * from employee where card_no='" + tokenNo + "'";
                PreparedStatement st = connection.prepareStatement(sql);
                ResultSet rs = st.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Duplicate Entry.");
                } else {
                    String sql_1 = "insert into employee"
                            + "(card_no,name,father_name,department,designation,pay_able_code,joining_date,"
                            + "date_of_desertion,address,gender,employee_type,reliever_status,"
                            + "active_status,reliever_type,wages_rate,"
                            + "sallery_type,reliever_allowance,special_allowance,extra_allowance,phone,ot_rate)"
                            + " values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
                    try (PreparedStatement statement = connection.prepareStatement(sql_1)) {
                        statement.setInt(1, tokenNo);
                        statement.setString(2, name);
                        statement.setString(3, fatherName);
                        statement.setString(4, department);
                        statement.setString(5, designation);
                        statement.setString(6, payableCode);
                        statement.setDate(7, sqlJoinDate);
                        statement.setDate(8, sqlDesertionDate);
                        statement.setString(9, address);
                        statement.setString(10, gender);
                        statement.setString(11, employeeType);
                        statement.setInt(12, relieverStatus);
                        statement.setString(13, activeStatus);
                        statement.setString(14, relieverType);
                        statement.setInt(15, wagesRate);
                        statement.setString(16, saleryType);
                        statement.setInt(17, relieverAllowance);
                        statement.setInt(18, specialAllowance);
                        statement.setInt(19, extraAllowance);
                        statement.setString(20, phone);
                        statement.setInt(21, otRate);
                        statement.executeUpdate();
                    }

                    showEmployeeTable();
                    initFields();
                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, "Error!!! Process Failed.");
            }

        }

    }

