    private void btn_add_userActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_userActionPerformed
        // TODO add your handling code here:
        String designationName = designation_name_tf.getText();
        String designationCode = designation_code_tf.getText();
        if (designationName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "please insert designation name.");
        } else {
            try {

                String sql = "select * from designation where designation='" + designationName + "' or "
                        + "code ='" + designationCode + "'";
                PreparedStatement st = connection.prepareStatement(sql);
                ResultSet rs = st.executeQuery();

                if (rs.next()) {
                    JOptionPane.showMessageDialog(null,designationName + " is exist!");
                    //userNameTF.requestFocus();
                    st.close();
                    rs.close();
                } else {

                    String sql_1 = "insert into designation"
                            + "(designation,code)"
                            + " values(?,?)";
                    try (PreparedStatement statement2 = connection.prepareStatement(sql_1)) {
                        statement2.setString(1, designationName);
                        statement2.setString(2, designationCode);                  
                        statement2.executeUpdate();
                    }
                    //JOptionPane.showMessageDialog(this, "New Department entry successful.");
                    clearFields();

                }
                showDesignationTable();

            } catch (HeadlessException | SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_add_userActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:

        String departmentCode = designation_code_tf.getText();
        String departmentName = designation_name_tf.getText();

        if (departmentName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Department name must not empty.");
        }else if(designationId==0){
            JOptionPane.showMessageDialog(this, "Please select a record for update.");
        }
        
        else {
            
            
            try {
                String sql = "update designation set designation = '" + departmentName + "',"
                        + "code = '" + departmentCode + "'"
                        + " where id='" + designationId + "'";
                PreparedStatement statement = connection.prepareStatement(sql);
                statement.executeUpdate();
                JOptionPane.showMessageDialog(this, "Update record successful.");
            } catch (HeadlessException | SQLException e) {
                 JOptionPane.showMessageDialog(this, "Update record is not successful.");
            }
            showDesignationTable();
            
        }


    }//GEN-LAST:event_btn_updateActionPerformed

    private void designation_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_designation_tableMouseClicked
        // TODO add your handling code here:
        int row = designation_table.getSelectedRow();
        String taleClicked = (designation_table.getModel().getValueAt(row, 1).toString());

        try {

            String sql = "select * from designation where designation='" + taleClicked + "'";
            PreparedStatement st = connection.prepareStatement(sql);
            ResultSet rs = st.executeQuery();

            if (rs.next()) {
                designationId = rs.getInt("id");
                designation_name_tf.setText(rs.getString("designation"));
                designation_code_tf.setText(rs.getString("code"));
                st.close();
                rs.close();
            }

        } catch (SQLException e) { 
            e.printStackTrace();
        }


    }//GEN-LAST:event_designation_tableMouseClicked

