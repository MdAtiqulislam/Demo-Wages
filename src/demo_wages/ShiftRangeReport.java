    private String getDepartmentCode(String dptDisplayName) {
        try {
            String sql = "select department_code from department where display_name='" + dptDisplayName + "'";
            PreparedStatement statement = connection.prepareStatement(sql);
            ResultSet rs = statement.executeQuery();
            if (rs.next()) {
                return rs.getString("department_code");
            } else {
                return null;
            }
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }

