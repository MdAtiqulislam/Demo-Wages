    private void getDepartmentFrom() {

        departmentCombo.removeAllItems();
        departmentToCombo.removeAllItems();
        dm.addElement("Select One");
        dm2.addElement("Select One");
        try {
            String sql = "select * from department ORDER BY department_code ASC";
            PreparedStatement statement = connection.prepareStatement(sql);
            ResultSet rs = statement.executeQuery();
            while (rs.next()) {
                dm.addElement(rs.getString("display_name"));
                dm2.addElement(rs.getString("display_name"));
                departmentCombo.setModel(dm);
                departmentToCombo.setModel(dm2);
            }
            statement.close();
            rs.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

