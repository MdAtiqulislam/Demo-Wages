    private void getData() {
        quality = qualityTF.getText();
        try {
            ounce = Double.parseDouble(ozTF.getText());
            rate = Double.parseDouble(rateTF.getText());
        } catch (NumberFormatException numberFormatException) {
            numberFormatException.printStackTrace();
        }
        remark = remarkTF.getText();
    }

    private boolean fieldsNotEmpty() {
        if (quality.isEmpty() || ounce <= 0 || rate <= 0) {
            return false;
        } else {
            return true;
        }
    }

    private boolean checkData() {
        try {
            String sql = "select * from product where quality='" + quality + "'";
            PreparedStatement st = connection.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            return rs.next();
        } catch (Exception e) {
            e.printStackTrace();
            return true;
        }
    }

    private void insertData() {
        try {
            String sql = "insert into product"
                    + "(quality,oz,rate,remark)"
                    + " values(?,?,?,?)";
            PreparedStatement statement = connection.prepareStatement(sql);
            statement.setString(1, quality);
            statement.setDouble(2, ounce);
            statement.setDouble(3, rate);
            statement.setString(4, remark);
            statement.executeUpdate();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clearFields() {
        qualityTF.setText("");
        ozTF.setText("");
        rateTF.setText("");
        remarkTF.setText("");
    }

    private void showProductsTable() {
        try {
            String sql_1 = "select quality, oz, rate, remark from product ORDER BY quality DESC";
            PreparedStatement st = connection.prepareStatement(sql_1);
            ResultSet rs_1 = st.executeQuery();
            productsTable.setModel(DbUtils.resultSetToTableModel(rs_1));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }

    private void setSelectItem() {
        int row = productsTable.getSelectedRow();
        String taleClicked = (productsTable.getModel().getValueAt(row, 0).toString());
        System.out.println(taleClicked);

        try {

            String sql = "select * from product where quality='" + taleClicked + "'";
            PreparedStatement st = connection.prepareStatement(sql);
            ResultSet rs = st.executeQuery();

            if (rs.next()) {
                qualityTF.setText(rs.getString("quality"));
                ozTF.setText(rs.getString("oz"));
                rateTF.setText(rs.getString("rate"));
                remarkTF.setText(rs.getString("remark"));

                ozTF.requestFocus();
                ozTF.selectAll();
                st.close();
                rs.close();
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void updateData() {
        try {
            String sql = "update product set oz = '" + ounce + "',rate='"+rate+"', "
                        + "remark = '" + remark + "'"
                        + " where quality='" + quality + "'";
                PreparedStatement statement = connection.prepareStatement(sql);
                statement.executeUpdate();
                
        } catch (Exception e) {
        }

    }

    private void deleteData(String key) {
        try {
            String sql="delete from product where quality='"+key+"'";
            PreparedStatement statement=connection.prepareStatement(sql);
            statement.executeUpdate();
        } catch (Exception e) {
            e.printStackTrace();
        }
            }
